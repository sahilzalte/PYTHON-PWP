# 1. Create a list
my_list = [10, 20, 30, 40, 50]
print("Original List:", my_list)

# 2. Access a list (Accessing elements by index)
print("Element at index 2:", my_list[2]) 

# 3. Update a list (Modifying an element at a specific index)
my_list[2] = 100 
print("Updated List:", my_list)

# 4. Delete a list (Deleting an element from the list)
del my_list[4]  
print("List after deletion:", my_list)